// Números
let numberVar = 10;
const PI = 3.14;
console.log("Números:", numberVar, PI);

// Strings
let stringVar = "Hello";
const myName = 'John';
console.log("Strings:", stringVar, myName);

// Lógicos
let boolVar = true;
const isTrue = false;
console.log("Lógicos:", boolVar, isTrue);

// Objetos
let objVar = { name: 'John', age: 30 };
const person = { name: 'Alice', age: 25 };
console.log("Objetos:", objVar, person);

// Null e undefined
let nullVar = null;
let undefinedVar;
console.log("Null e Undefined:", nullVar, undefinedVar);

// Operadores aritméticos
let resultAdd = 10 + 5; // Adição
let resultSub = 10 - 5; // Subtração
let resultMul = 10 * 5; // Multiplicação
let resultDiv = 10 / 5; // Divisão
let resultMod = 10 % 3; // Módulo
console.log("Operadores Aritméticos:", resultAdd, resultSub, resultMul, resultDiv, resultMod);

// Operadores lógicos
let resultAnd = true && false; // AND lógico
let resultOr = true || false; // OR lógico
let resultNot = !true; // NOT lógico
console.log("Operadores Lógicos:", resultAnd, resultOr, resultNot);

// Operadores de igualdade (ampla e estrita)
let num1 = 10;
let num2 = "10";
let looseEquality = num1 == num2; // Igualdade ampla
let strictEquality = num1 === num2; // Igualdade estrita
console.log("Operadores de Igualdade: Igualdade ampla:", looseEquality, "Igualdade estrita:", strictEquality);
